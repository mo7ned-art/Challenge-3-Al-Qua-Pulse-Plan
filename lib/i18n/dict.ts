export type DictKey = keyof (typeof DICT)["en"]

export function t(lang: Language, key: DictKey | string): string {
  const dict = DICT[lang] as unknown as Record<string, string | readonly string[]>
  const val = dict[key]
  if (typeof val === "string") return val
  if (Array.isArray(val)) return (val as readonly string[]).join("\n")
  // Fallback to English
  const en = DICT.en as unknown as Record<string, string | readonly string[]>
  const enVal = en[key]
  if (typeof enVal === "string") return enVal
  if (Array.isArray(enVal)) return (enVal as readonly string[]).join("\n")
  return String(key)
}

/** Format a string with simple {n} placeholders. */
export function format(template: string, vars: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`))
}
